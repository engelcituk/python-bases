keys = diccionario.keys() #otenemos todas las llaves
valores = diccionario.values() #otenemos todos los valores
items = diccionario.items() #otenemos todos los items (llave y valor)
tupla = tuple(keys) #se puede transformar las llaves en tupla
lista = list(keys) #se puede transformar las llaves en lista
listaValores = list(valores) #se puede transformar los valores en una lista


print(tupla)
print(lista)
print(listaValores)
